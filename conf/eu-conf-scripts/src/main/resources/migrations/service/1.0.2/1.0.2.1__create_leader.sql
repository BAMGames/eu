CREATE TABLE T_LEADER (
  ID     BIGINT NOT NULL AUTO_INCREMENT,
  CODE VARCHAR(255),
  NAME VARCHAR(255),
  R_COUNTRY VARCHAR(255),
  EVENT VARCHAR(255),
  BEGIN INTEGER,
  END INTEGER,
  RANK VARCHAR(255),
  MANOEUVRE INTEGER,
  FIRE INTEGER,
  SHOCK INTEGER,
  SIEGE INTEGER,
  TYPE VARCHAR(255),
  ROTW   BIT,
  ASIA   BIT,
  AMERICA   BIT,
  PRIVATEER   BIT,
  MZIN BIT,
  T_LEADER VARCHAR(255),
  PRIMARY KEY (ID)
);